public class CodeMValue {

    long id;
    private String keyCd;
    private String keySubCd;
    private String cdValue;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getKeyCd() {
        return keyCd;
    }

    public void setKeyCd(String keyCd) {
        this.keyCd = keyCd;
    }

    public String getKeySubCd() {
        return keySubCd;
    }

    public void setKeySubCd(String keySubCd) {
        this.keySubCd = keySubCd;
    }

    public String getCdValue() {
        return cdValue;
    }

    public void setCdValue(String cdValue) {
        this.cdValue = cdValue;
    }


}
